    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
      if (tblPersonagens.getSelectedRow() != -1) {
            PersonagemVamp obj = (PersonagemVamp) tblPersonagens.getModel().
                    getValueAt(tblPersonagens.getSelectedRow(), 0);
            int op_remover = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover " + obj + "?");
            if (op_remover == JOptionPane.YES_OPTION) {
                try {
                    dao.remover(obj);
                    JOptionPane.showMessageDialog(rootPane, "Criador removido com sucesso... ");
                    loadTabelaPersonagem();
                } catch (Exception ex) {
                    System.err.println("Erro ao remover Criador: " + ex);
                }
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um cliente");
        }
                   
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
    CadastroVampiro1JD telaCadastro = new CadastroVampiro1JD(this, true);
    telaCadastro.setLocationRelativeTo(this);
    telaCadastro.setVisible(true);

        PersonagemVamp novo = telaCadastro.getPersonagem();

    if (novo != null) {
        try {
            dao.persist(novo);
            loadTabelaPersonagem();
        } catch (Exception ex) {
            System.err.println("Erro ao adicionar Personagem: " + ex);
        }
    }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
         if (tblPersonagens.getSelectedRow() != -1) {
            PersonagemVamp obj = (PersonagemVamp) tblPersonagens.getModel().
                    getValueAt(tblPersonagens.getSelectedRow(), 0);

            CadastroVampiro1JD telaEdicao = new CadastroVampiro1JD(this, rootPaneCheckingEnabled);
            telaEdicao.setPersonagem(obj);

            telaEdicao.setVisible(true);

            
            PersonagemVamp obj_retornado = telaEdicao.getPersonagem();
                    
        if (obj_retornado != null) {
            
            try {
                dao.persist(obj_retornado);
                loadTabelaPersonagem();
            } catch (Exception ex) {
                System.err.println("Erro ao editar Personagem: " + ex);
            }
        }
       }
    }//GEN-LAST:event_btnEditarActionPerformed

