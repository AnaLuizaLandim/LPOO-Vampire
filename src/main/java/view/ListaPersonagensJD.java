    public void loadTabelaPersonagem() {

        // Obtém o modelo da tabela - vincular o que definimos no Desing
        // Obtém o modelo da tabela - vincular o que definimos no Desing
        DefaultTableModel modelo = (DefaultTableModel) tblPersonagens.getModel();
        //limpar as linhas e popular 
        modelo.setNumRows(0);

        for (PersonagemVamp obj : dao.listaPersonagemVamp()) {
            Object[] linha = {
                obj,
                obj.getClan()
            };
            modelo.addRow(linha);
        }

    }
    
   

